def create_interactive(image=None, name=None, ram=None, cpu=None, port=None, env=None, volume=None, paper=False):
    if not image:
        image = Prompt.ask("  Docker image", default="nginx:alpine")

    is_game = paper or "minecraft" in image.lower()

    def _parse_ports(p_list):
        result = {}
        for p in (p_list or []):
            parts = p.split(":")
            if len(parts) == 2:
                h_port, rest = parts
                if "/" in rest:
                    c_port, proto = rest.split("/", 1)
                else:
                    c_port = rest
                    proto = "tcp"
                try:
                    result[f"{c_port}/{proto}"] = int(h_port)
                except ValueError:
                    pass
        return result

    def _parse_env(e_list):
        result = {}
        for e in (e_list or []):
            if "=" in e:
                k, v = e.split("=", 1)
                result[k.strip()] = v.strip()
        return result

    def _parse_volumes(v_list):
        result = {}
        for v in (v_list or []):
            parts = v.split(":", 1)
            if len(parts) == 2:
                host_path, container_path = parts
                host_path = os.path.expanduser(host_path)
                if not os.path.exists(host_path):
                    os.makedirs(host_path, exist_ok=True)
                result[host_path] = {"bind": container_path, "mode": "rw"}
        return result

    ports_input = _parse_ports(port)
    env_input = _parse_env(env)
    volumes_input = _parse_volumes(volume)

    if not port:
        ports_input = _ask_ports()
    else:
        for c_p in list(ports_input.keys()):
            console.print(f"  Port: [cyan]{ports_input[c_p]}:{c_p}[/cyan]")

    if not env:
        env_input = _ask_env()
    else:
        for k, v in env_input.items():
            console.print(f"  Env: [dim]{k}={v}[/dim]")

    if not volume:
        volumes_input = _ask_volumes()
    else:
        for h_p in list(volumes_input.keys()):
            console.print(f"  Volume: {h_p} [dim]→ {volumes_input[h_p]['bind']}[/dim]")

    if ram and cpu:
        ram_cfg, cpu_cfg = ram, cpu
    else:
        ram_cfg, cpu_cfg = _ask_resources(ram or "512m", cpu or "1")

    paper_info = None
    if is_game:
        result = _ask_paper_version()
        if result:
            paper_info = result

    suggested_name = name or f"{image.split('/')[-1].split(':')[0]}-{os.urandom(4).hex()}"
    name = Prompt.ask("  Container name", default=suggested_name)

    _show_creation_summary(name, ports_input, env_input, volumes_input, ram_cfg, cpu_cfg,
                          paper_version=paper_info[0] if paper_info else None)

    if not Confirm.ask("\n[bold]Build this container?[/bold]", default=True):
        console.print("[yellow]Cancelled[/yellow]")
        return

    build_and_start(image, name, ports_input, env_input, volumes_input, ram_cfg, cpu_cfg,
                   paper_info=paper_info)


def run_custom(image, name, ram, cpu):
    """Quick custom container: prompts for ports/volumes/env but skips template selection."""
    is_game = "minecraft" in image.lower()

    if not ram and not cpu:
        ram, cpu = _ask_resources()

    paper_info = None
    if is_game:
        result = _ask_paper_version()
        if result:
            paper_info = result

    ports = _ask_ports()
    volumes = _ask_volumes()
    env_vars = _ask_env()

    suggested_name = name or f"{image.split('/')[-1].split(':')[0]}-{os.urandom(4).hex()}"
    name = Prompt.ask("  Container name", default=suggested_name)

    _show_creation_summary(name, ports, env_vars, volumes, ram, cpu,
                          paper_version=paper_info[0] if paper_info else None)

    if not Confirm.ask("\n[bold]Build this container?[/bold]", default=True):
        console.print("[yellow]Cancelled[/yellow]")
        return

    build_and_start(image, name, ports, env_vars, volumes, ram, cpu, paper_info=paper_info)
